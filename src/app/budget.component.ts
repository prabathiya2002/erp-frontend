  exportBudgets() {
    if (!this.filteredBudgets || this.filteredBudgets.length === 0) {
      alert('No budgets to export. Please create budgets first.');
      return;
    }

    // Prepare budget summary data
    const budgetSummary = this.filteredBudgets.map(budget => ({
      'Budget Name': budget.name,
      'Fiscal Year': budget.fiscalYear,
      'Period': budget.period,
      'Type': budget.type,
      'Status': budget.status,
      'Start Date': budget.startDate,
      'End Date': budget.endDate,
      'Total Lines': budget.budgetLines?.length || 0,
      'Description': budget.description || ''
    }));

    // Create workbook
    const wb = XLSX.utils.book_new();

    // Add Budget Summary sheet
    const wsSummary = XLSX.utils.json_to_sheet(budgetSummary);
    wsSummary['!cols'] = [
      { wch: 25 }, { wch: 12 }, { wch: 12 }, { wch: 15 },
      { wch: 12 }, { wch: 12 }, { wch: 12 }, { wch: 12 }, { wch: 40 }
    ];
    XLSX.utils.book_append_sheet(wb, wsSummary, 'Budget Summary');

    // Add detailed budget lines for each budget
    this.filteredBudgets.forEach((budget, index) => {
      if (budget.budgetLines && budget.budgetLines.length > 0) {
        const budgetLines = budget.budgetLines.map(line => {
          const account = this.accounts.find(a => a.id === line.accountId);
          return {
            'Account ID': line.accountId,
            'Account': account ? `${account.code} - ${account.name}` : 'Unknown',
            'Line Type': line.lineType,
            'Amount': line.amount,
            'Notes': line.notes || ''
          };
        });

        const wsLines = XLSX.utils.json_to_sheet(budgetLines);
        wsLines['!cols'] = [{ wch: 12 }, { wch: 30 }, { wch: 15 }, { wch: 15 }, { wch: 40 }];
        
        // Limit sheet name to 31 characters (Excel limit)
        const sheetName = budget.name.substring(0, 28) + (index + 1);
        XLSX.utils.book_append_sheet(wb, wsLines, sheetName);
      }
    });

    // Generate filename with current date
    const date = new Date().toISOString().split('T')[0];
    const filename = `Budgets_${date}.xlsx`;

    // Save file
    XLSX.writeFile(wb, filename);
  }
  

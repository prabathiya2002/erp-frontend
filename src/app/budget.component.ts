// Interface: Defines the structure of a Budget object
// This ensures type safety - we know exactly what properties a budget should have
interface Budget {
  id?: number;                    // Optional: Auto-generated by database when budget is saved
  name: string;                   // Budget name (e.g., "2026 Operating Budget")
  description: string;            // Detailed description of the budget
  fiscalYear: number;             // Year this budget applies to (e.g., 2026)
  period: string;                 // MONTHLY, QUARTERLY, or ANNUALLY
  type: string;                   // OPERATING, CAPITAL, CASH_FLOW, or MASTER
  status: string;                 // DRAFT, SUBMITTED, APPROVED, ACTIVE, or CLOSED
  startDate: string;              // When budget period starts (YYYY-MM-DD format)
  endDate: string;                // When budget period ends
  createdDate?: string;           // Optional: When budget was created
  approvedDate?: string;          // Optional: When budget was approved
  approvedBy?: string;            // Optional: Who approved the budget
  notes: string;                  // Additional comments or notes
  budgetLines?: BudgetLine[];     // Optional: Array of line items in the budget
}

// Interface: Represents one line item in a budget
// Example: "Salaries Account - $50,000 budgeted for Q1"
interface BudgetLine {
  id?: number;            // Optional: Auto-generated ID
  accountId: number;      // Which account this budget line is for (e.g., Salaries, Rent)
  amount: number;         // Dollar amount budgeted for this account
  notes: string;          // Additional notes about this budget line
  lineType?: string;      // Optional: REVENUE or EXPENSE
}

// Interface: Represents a financial account from Chart of Accounts
// Used in dropdowns so user can select which account to budget for
interface Account {
  id: number;       // Unique identifier
  code: string;     // Account code (e.g., "1000", "4000")
  name: string;     // Account name (e.g., "Cash", "Sales Revenue")
  type: string;     // ASSET, LIABILITY, EQUITY, REVENUE, or EXPENSE
  status: string;   // ACTIVE or INACTIVE
}

// Interface: Variance Analysis compares budgeted amounts vs actual amounts
// This shows how well the company is sticking to its budget
interface VarianceAnalysis {
  budgetId: number;       // Which budget we're analyzing
  budgetName: string;     // Name of the budget
  period: string;         // Time period being analyzed
  startDate: string;      // Start of analysis period
  endDate: string;        // End of analysis period
  lines: any[];           // Detailed line-by-line comparison
  summary: {              // Overall summary of variances
    totalBudgetRevenue: number;     // How much revenue was budgeted
    totalActualRevenue: number;     // How much revenue actually came in
    revenueVariance: number;        // Difference (positive = better than expected)
    totalBudgetExpense: number;     // How much expense was budgeted
    totalActualExpense: number;     // How much was actually spent
    expenseVariance: number;        // Difference (negative = spent less than budgeted)
    budgetNetIncome: number;        // Expected profit (Revenue - Expense)
    actualNetIncome: number;        // Actual profit
    netIncomeVariance: number;      // How much better/worse than expected
  };
}

